    public void refresh(){
        for (Map.Entry<Integer,Order> entry : de.getOrders().getOlist().entrySet()){
            Order or = entry.getValue();
            Object[] row = new Object[6];
            int id = or.getId();
            String content = or.getContent();
            String res = or.getRestaurant().getName();
            String cu = or.getCustomer().getName();
            String status = or.getStatus();
            String comment = or.getComment();
            row[0] = id;
            row[1] = content;
            row[2] = res;
            row[3] = cu;
            row[4] = status;
            row[5] = comment;
            model.addRow(row);
            System.out.print(id);
        }}
