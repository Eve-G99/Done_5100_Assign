    private void asbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_asbtnActionPerformed
        // TODO add your handling code here:
        int orid = Integer.valueOf(orderselected.getText().toString());
        int deid = Integer.valueOf(deliverymanselected.getText().toString());
        Order or = res.getOrders().getOlist().get(orid);
        DeliveryMan de = system.getDeliveryManDirectory().getDelivermen().get(deid);
        or.setStatus("Delivering");
        or.setDeliveryman(de);
        de.getOrders().getOlist().put(orid, or);
        model2.removeRow(selected1);
        model3.setValueAt(de.unfinished(), selected2, 2);
        Object[] row = new Object[6];
        row[0] = or.getId();
            row[1] = or.getContent();
            row[2] = or.getDeliveryman().getName();
            row[3] = or.getCustomer().getName();
            row[4] = or.getStatus();
            row[5] = or.getComment();
        model1.addRow(row);
        JOptionPane.showMessageDialog(this, "Assigned.");
    }//GEN-LAST:event_asbtnActionPerformed

