        public void refresh(){
        for (Map.Entry<Integer,Order> entry : res.getOrders().getOlist().entrySet()){
            Order or = entry.getValue();
            if (or.getStatus().equals("Finished") || or.getStatus().equals("Refused") || or.getStatus().equals("Delivering"))
            {
            Object[] row = new Object[6];
            int id = or.getId();
            String content = or.getContent();
            String customer = or.getCustomer().getName();
            String status = or.getStatus();
            String comment = or.getComment();
            String de;
            if (or.getStatus().equals("Finished") || or.getStatus().equals("Delivering"))
                de = or.getDeliveryman().getName();
            else
                de = "";
            row[0] = id;
            row[1] = content;
            row[2] = de;
            row[3] = customer;
            row[4] = status;
            row[5] = comment;
            model1.addRow(row);}
            
            else {
                Object[] row = new Object[2];
                int id = or.getId();
                String content = or.getContent();
                row[0] = id;
                row[1] = content;
                model2.addRow(row);
            }   
        }
        for (Map.Entry<Integer,DeliveryMan> entry :system.getDeliveryManDirectory().getDelivermen().entrySet()){
            DeliveryMan de = entry.getValue();
            int id = de.getId();
            String name = de.getName();
            int cnt = de.unfinished();
            Object[] row = new Object[3];
            row[0] = id;
            row[1] = name;
            row[2] = cnt;
            model3.addRow(row);
        }
    }
